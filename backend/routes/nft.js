const contractAddress = "0xA30C990fcda532F85CA3C52c744A373F71FF0299";
const provider = new ethers.JsonRpcProvider("https://sepolia.infura.io/v3/108414fd328b4a7e8ca0955d0ea97eb2");
const contract = new ethers.Contract(contractAddress, contractABI, provider);

// --- Your route ---
router.get('/certificates', async (req, res) => {
  const walletAddress = req.query.walletAddress;
  console.log('Received walletAddress:', walletAddress);
  if (!walletAddress) {
    return res.status(400).json({ error: 'walletAddress query parameter is required' });
  }

  try {
   const checksumAddress = getAddress(walletAddress);
    const filter = contract.filters.CertificateMinted(checksumAddress);
    const events = await contract.queryFilter(filter);

    const certificates = [];
    for (const event of events) {
      const tokenId = event.args.tokenId.toString();
      const tokenURI = event.args.tokenURI;
      let metadata = {};
      if (tokenURI && tokenURI.startsWith('ipfs://')) {
        const ipfsHash = tokenURI.replace('ipfs://', '');
        try {
          const metadataResponse = await fetch(`https://ipfs.io/ipfs/${ipfsHash}`);
          metadata = await metadataResponse.json();
        } catch (error) {
          console.error('Error fetching metadata:', error);
        }
      }
      certificates.push({
        tokenId,
        tokenURI,
        metadata,
        contract_address: contractAddress
      });
    }

    res.json(certificates);
  } catch (error) {
    console.error('Error fetching certificates:', error.stack || error);
    res.status(500).json({ error: 'Failed to fetch certificates' });
  }
});

module.exports = router;
